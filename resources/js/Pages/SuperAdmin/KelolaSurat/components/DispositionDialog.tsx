import {
    Dialog,
    DialogContent,
    DialogDescription,
    DialogHeader,
    DialogTitle,
} from '@/Components/ui/dialog';
import { Badge } from '@/Components/ui/badge';
import { Button } from '@/Components/ui/button';
import { Label } from '@/Components/ui/label';
import { ScrollArea } from '@/Components/ui/scroll-area';
import { Textarea } from '@/Components/ui/textarea';
import { Download, Eye, FileText, SendHorizontal } from 'lucide-react';
import { LetterRecord } from '@/Pages/SuperAdmin/KelolaSurat/components/LettersTable';
import type { InertiaFormProps } from '@inertiajs/react';

interface DispositionDialogProps {
    open: boolean;
    onOpenChange: (value: boolean) => void;
    targets: LetterRecord[];
    dispositionForm: InertiaFormProps<{
        disposition_note: string;
        letter_ids: number[];
    }>;
    onSubmit: (mode: 'forward' | 'reject') => void;
}

export default function DispositionDialog({
    open,
    onOpenChange,
    targets,
    dispositionForm,
    onSubmit,
}: DispositionDialogProps) {
    return (
        <Dialog open={open} onOpenChange={onOpenChange}>
            <DialogContent className="border-0 bg-white p-0 sm:max-w-lg max-h-[90vh] overflow-y-auto">
                <DialogHeader className="space-y-1 border-b border-slate-100 px-6 py-4">
                    <DialogTitle>Disposisi Surat</DialogTitle>
                    <DialogDescription>
                        Tambahkan catatan singkat sebelum meneruskan surat ke divisi tujuan.
                    </DialogDescription>
                </DialogHeader>
                {targets.length > 0 ? (
                    <div className="flex flex-col gap-5 px-6 pb-6 pt-4">
                        <div className="rounded-2xl border border-slate-200 bg-slate-50/70 p-4">
                            <div className="flex flex-wrap items-center justify-between gap-3">
                                <div>
                                    <p className="text-xs font-semibold uppercase tracking-wide text-slate-500">
                                        Surat terpilih
                                    </p>
                                    <p className="text-base font-semibold text-slate-900">
                                        {targets.length} dokumen siap dikirim
                                    </p>
                                </div>
                                <Badge variant="secondary" className="bg-blue-600 text-white">
                                    HR &rarr; Divisi Tujuan
                                </Badge>
                            </div>
                            <ScrollArea className="mt-4 max-h-64 pr-2">
                                <div className="space-y-3 pr-1">
                                    {targets.map((letter) => (
                                        <div
                                            key={letter.id}
                                            className="rounded-xl border border-white/60 bg-white p-3 shadow-sm"
                                        >
                                            <div className="flex items-start justify-between gap-3">
                                                <div>
                                                    <p className="text-sm font-semibold text-slate-900">
                                                        {letter.letterNumber}
                                                    </p>
                                                    <p className="text-xs text-slate-500">{letter.subject}</p>
                                                </div>
                                                <Badge variant="outline" className="text-slate-600">
                                                    {letter.targetDivision ?? '-'}
                                                </Badge>
                                            </div>
                                            <p className="mt-1 text-xs text-slate-500">
                                                {letter.senderName} - {letter.date}
                                            </p>
                                            {letter.attachment?.url && (
                                                <div className="mt-2 flex items-center gap-2 rounded-lg border border-slate-100 bg-slate-50/80 px-2 py-1 text-xs text-slate-600">
                                                    <FileText className="h-3.5 w-3.5 text-blue-500" />
                                                    <span className="flex-1 truncate">
                                                        {letter.attachment?.name ?? 'Lampiran'}
                                                    </span>
                                                    <Button variant="ghost" size="icon" className="h-7 w-7 text-slate-500" asChild>
                                                        <a href={letter.attachment.url} target="_blank" rel="noreferrer">
                                                            <Eye className="h-3.5 w-3.5" />
                                                            <span className="sr-only">Lihat lampiran</span>
                                                        </a>
                                                    </Button>
                                                    <Button variant="ghost" size="icon" className="h-7 w-7 text-slate-500" asChild>
                                                        <a
                                                            href={letter.attachment.url}
                                                            target="_blank"
                                                            rel="noreferrer"
                                                            download={letter.attachment.name ?? undefined}
                                                        >
                                                            <Download className="h-3.5 w-3.5" />
                                                            <span className="sr-only">Unduh lampiran</span>
                                                        </a>
                                                    </Button>
                                                </div>
                                            )}
                                        </div>
                                    ))}
                                </div>
                            </ScrollArea>
                        </div>
                        <div className="space-y-2">
                            <Label htmlFor="disposition-note">Catatan (opsional)</Label>
                            <Textarea
                                id="disposition-note"
                                rows={4}
                                placeholder="Tambahkan konteks sebelum surat diteruskan..."
                                value={dispositionForm.data.disposition_note}
                                onChange={(event) =>
                                    dispositionForm.setData('disposition_note', event.target.value)
                                }
                            />
                            {dispositionForm.errors.disposition_note && (
                                <p className="text-xs text-red-500">
                                    {dispositionForm.errors.disposition_note}
                                </p>
                            )}
                        </div>
                        <div className="grid gap-2 sm:grid-cols-2">
                            <Button
                                type="button"
                                variant="outline"
                                className="w-full border-rose-200 text-rose-600 hover:bg-rose-50"
                                disabled={dispositionForm.processing || targets.length === 0}
                                onClick={() => onSubmit('reject')}
                            >
                                {dispositionForm.processing ? 'Memproses...' : 'Tolak Surat'}
                            </Button>
                            <Button
                                onClick={() => onSubmit('forward')}
                                disabled={dispositionForm.processing || targets.length === 0}
                                className="w-full gap-2 bg-blue-600 text-white hover:bg-blue-700"
                            >
                                {dispositionForm.processing ? (
                                    'Memproses...'
                                ) : (
                                    <span className="flex items-center justify-center gap-2">
                                        <SendHorizontal className="h-4 w-4" />
                                        Disposisi ke Divisi
                                    </span>
                                )}
                            </Button>
                        </div>
                    </div>
                ) : (
                    <div className="px-6 py-10 text-center text-sm text-slate-500">
                        Pilih surat yang ingin didisposisi.
                    </div>
                )}
            </DialogContent>
        </Dialog>
    );
}

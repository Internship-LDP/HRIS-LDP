import { PageProps } from '@/types';
import { Link, router, usePage } from '@inertiajs/react';
import {
    LayoutDashboard,
    LogOut,
    Menu,
    User,
    X,
    ChevronDown,
    Mail,
} from 'lucide-react';
import { useState, useEffect, useRef } from 'react';
import {
    DropdownMenu,
    DropdownMenuContent,
    DropdownMenuItem,
    DropdownMenuLabel,
    DropdownMenuSeparator,
    DropdownMenuTrigger,
} from '@/Components/ui/dropdown-menu';
import { Button } from '@/Components/ui/button';

interface NavbarProps { }

interface NavItem {
    label: string;
    icon: any;
    routeName: string;
    pattern?: string | string[];
    notificationKey?: string;
}

const navItems: NavItem[] = [
    {
        label: 'Dashboard',
        icon: LayoutDashboard,
        routeName: 'admin-staff.dashboard',
        pattern: 'admin-staff.dashboard'
    },
    {
        label: 'Kelola Surat',
        icon: Mail,
        routeName: 'admin-staff.letters',
        pattern: 'admin-staff.letters',
        notificationKey: 'admin-staff.letters'
    },
];

export default function Navbar({ }: NavbarProps) {
    const { props: { auth, sidebarNotifications = {} } } = usePage<PageProps>();
    const user = auth?.user;
    const profilePhotoUrl = auth?.profilePhotoUrl;
    const [isMobileMenuOpen, setIsMobileMenuOpen] = useState(false);
    const [isVisible, setIsVisible] = useState(true);
    const [isDropdownOpen, setIsDropdownOpen] = useState(false);
    const lastScrollY = useRef(0);
    const scrollTimeout = useRef<NodeJS.Timeout | null>(null);

    // Active check helper
    const isActive = (patterns: string | string[] | undefined) => {
        if (!patterns) return false;
        if (Array.isArray(patterns)) {
            return patterns.some((pattern) => route().current(pattern));
        }
        return route().current(patterns);
    };

    useEffect(() => {
        const handleScroll = () => {
            if (scrollTimeout.current) {
                clearTimeout(scrollTimeout.current);
            }

            scrollTimeout.current = setTimeout(() => {
                const currentScrollY = window.scrollY;

                if (isDropdownOpen) return;

                if (currentScrollY < 10) {
                    setIsVisible(true);
                } else if (currentScrollY > lastScrollY.current && currentScrollY > 150) {
                    setIsVisible(false);
                    setIsMobileMenuOpen(false);
                } else if (currentScrollY < lastScrollY.current - 10) {
                    setIsVisible(true);
                }

                lastScrollY.current = currentScrollY;
            }, 150);
        };

        window.addEventListener('scroll', handleScroll, { passive: true });

        return () => {
            window.removeEventListener('scroll', handleScroll);
            if (scrollTimeout.current) {
                clearTimeout(scrollTimeout.current);
            }
        };
    }, [isDropdownOpen]);

    return (
        <nav
            className={`
                fixed top-0 left-0 right-0 z-50 bg-blue-900 border-b border-blue-800 shadow-sm
                transition-transform duration-500 ease-in-out
                ${isVisible ? 'translate-y-0' : '-translate-y-full'}
            `}
        >
            <div className="w-full px-4 sm:px-6 lg:px-8">
                <div className="flex items-center justify-between h-16">
                    {/* Logo/Brand - Left */}
                    <div className="flex items-center gap-3">
                        <div className="flex items-center gap-2">
                            <img
                                src="/img/LogoLDP.png"
                                alt="LDP Logo"
                                className="h-10 w-10 object-contain brightness-0 invert"
                            />
                            <div className="hidden sm:block">
                                <p className="text-[9px] uppercase tracking-widest text-blue-200 font-semibold">
                                    PT. Lintas Data Prima
                                </p>
                                <p className="text-sm font-bold text-white">Admin Staff Portal</p>
                            </div>
                        </div>
                    </div>

                    {/* Desktop Navigation - Center */}
                    <div className="hidden md:flex items-center gap-1 absolute left-1/2 -translate-x-1/2">
                        {navItems.map((item) => {
                            const Icon = item.icon;
                            const active = isActive(item.pattern);
                            const notificationCount = sidebarNotifications?.[item.notificationKey ?? ''] ?? 0;

                            return (
                                <Link
                                    key={item.routeName}
                                    href={route(item.routeName)}
                                    className={`
                                        flex items-center gap-2 px-4 py-2 rounded-lg text-sm font-medium
                                        transition-all relative
                                        ${active
                                            ? 'bg-white/15 text-white'
                                            : 'text-blue-100 hover:bg-white/10 hover:text-white'
                                        }
                                    `}
                                >
                                    <Icon className="h-4 w-4" />
                                    <span>{item.label}</span>
                                    {notificationCount > 0 && (
                                        <span className="ml-1 inline-flex h-5 min-w-[1.25rem] items-center justify-center rounded-full bg-rose-500 px-1.5 text-[10px] font-bold text-white">
                                            {notificationCount > 99 ? '99+' : notificationCount}
                                        </span>
                                    )}
                                </Link>
                            );
                        })}
                    </div>

                    {/* User Profile & Mobile Toggle - Right */}
                    <div className="flex items-center gap-2 ml-auto">
                        <DropdownMenu open={isDropdownOpen} onOpenChange={setIsDropdownOpen}>
                            <DropdownMenuTrigger asChild>
                                <Button
                                    variant="ghost"
                                    className="flex items-center gap-2 px-2 md:px-3 py-2 h-auto hover:bg-white/10 text-white hover:text-white"
                                >
                                    <div className="h-8 w-8 rounded-full bg-blue-700 flex items-center justify-center overflow-hidden border border-blue-600">
                                        {profilePhotoUrl ? (
                                            <img
                                                src={profilePhotoUrl}
                                                alt="Foto profil"
                                                className="h-full w-full object-cover"
                                            />
                                        ) : (
                                            <User className="h-4 w-4 text-white" />
                                        )}
                                    </div>
                                    <div className="hidden md:block text-left">
                                        <p className="text-xs font-semibold text-white">{user?.name}</p>
                                        <p className="text-[10px] text-blue-200">{user?.email}</p>
                                    </div>
                                    <ChevronDown className="hidden md:block h-4 w-4 text-blue-300" />
                                </Button>
                            </DropdownMenuTrigger>
                            <DropdownMenuContent align="end" className="w-56">
                                <DropdownMenuLabel>
                                    <p className="text-xs font-semibold">{user?.name}</p>
                                    <p className="text-[10px] text-slate-500 font-normal">{user?.email}</p>
                                </DropdownMenuLabel>
                                <DropdownMenuSeparator />
                                {/* <DropdownMenuItem asChild>
                                    <Link
                                        href={route('profile.edit')}
                                        className="cursor-pointer flex items-center"
                                    >
                                        <User className="h-4 w-4 mr-2" />
                                        Profil
                                    </Link>
                                </DropdownMenuItem> 
                                <DropdownMenuSeparator /> */}
                                <DropdownMenuItem
                                    onClick={() => router.post(route('logout'))}
                                    className="text-red-600 focus:text-red-600 cursor-pointer"
                                    disabled={false}
                                >
                                    <LogOut className="h-4 w-4 mr-2" />
                                    Keluar
                                </DropdownMenuItem>
                            </DropdownMenuContent>
                        </DropdownMenu>

                        {/* Mobile Menu Toggle */}
                        <button
                            onClick={() => setIsMobileMenuOpen(!isMobileMenuOpen)}
                            className="md:hidden p-2 rounded-lg text-blue-100 hover:bg-white/10 hover:text-white"
                        >
                            {isMobileMenuOpen ? (
                                <X className="h-6 w-6" />
                            ) : (
                                <Menu className="h-6 w-6" />
                            )}
                        </button>
                    </div>
                </div>
            </div>

            {/* Mobile Menu */}
            {isMobileMenuOpen && (
                <div className="md:hidden border-t border-blue-800 bg-blue-900 shadow-xl">
                    <div className="px-4 py-3 space-y-1">
                        {navItems.map((item) => {
                            const Icon = item.icon;
                            const active = isActive(item.pattern);
                            const notificationCount = sidebarNotifications?.[item.notificationKey ?? ''] ?? 0;

                            return (
                                <Link
                                    key={item.routeName}
                                    href={route(item.routeName)}
                                    onClick={() => setIsMobileMenuOpen(false)}
                                    className={`
                                        flex items-center gap-3 px-3 py-3 rounded-lg text-sm font-medium
                                        transition-all
                                        ${active
                                            ? 'bg-white/15 text-white'
                                            : 'text-blue-100 hover:bg-white/10 hover:text-white'
                                        }
                                    `}
                                >
                                    <Icon className="h-4 w-4" />
                                    <span className="flex-1">{item.label}</span>
                                    {notificationCount > 0 && (
                                        <span className="inline-flex h-5 min-w-[1.25rem] items-center justify-center rounded-full bg-rose-500 px-1.5 text-[10px] font-bold text-white">
                                            {notificationCount > 99 ? '99+' : notificationCount}
                                        </span>
                                    )}
                                </Link>
                            );
                        })}
                    </div>
                </div>
            )}
        </nav>
    );
}

// Helper function to get city display name with type
export const getCityDisplayName = (city: City): string => {
    return `${city.type} ${city.name}`;
};

// Helper function to get all provinces
export const getAllProvinces = (): string[] => {
    return indonesianLocations.map(province => province.name);
};

// Helper function to get cities by province
export const getCitiesByProvince = (provinceName: string): City[] => {
    const province = indonesianLocations.find(p => p.name === provinceName);
    return province ? province.cities : [];
};
